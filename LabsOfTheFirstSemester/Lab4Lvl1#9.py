##поменять местами максимальный элемен с первым строк матрицы 5x7
a=[[1,3,4,5,4],[3,554,32,43,23],[32,42,332,23,23],[34,21,6,0,5],[5678,876,432,3234,54],[90,89,43,233,3],[54,33,3,4,3]]
print(a)
j=0
while j<=6:
    k=0
    for i in range(5):
        if a[j][i]>k:
            k=a[j][i]
            indj=j
            indi=i
    l=a[j][0]
    a[j][0]=k
    a[indj][indi]=l
    j+=1
print(a)
          
          
